				<h3>Thank you and Enjoy!</h3>
				The plugin is ideal for the websites owned by a developer or a company that has clients.<br/>
The plugin sends your clients an email that notifies them with your invoice. Record of your clients will be recorded in your database such as names, name of their companies, emails, contact numbers and etc. Email invoices are in html format , you can contact me if you want or need it to be customized.<br/>
Invoices that are created will be monitored by their statuses. You can send a reminder to your clients through the notification if payment is not yet made. If payment is completed, you can confirm the invoice that the payment is paid. You can also cancel an invoice that is already created.<br/>
			<p>Plugin URI: <a href="http://www.sherkspear.com/portfolio-item/smart-invoice-and-billing-management/" target="_blank">http://www.sherkspear.com/portfolio-item/smart-invoice-and-billing-management/</a></p>
			
			<h3>Installation</h3>
			<ol>
				<li>BACKUP everything before you install the plugin.</li>
				<li>Upload smart-invoice-and-billing-management directory to the `/wp-content/plugins/` directory</li>
				<li>Activate the plugin through the 'Plugins' menu in WordPress</li>	
				<li>Update the Permalinks on settings.</li>
				<li>Create Invoice by filling at the invoice fields for the invoice details, date covered, Company, the amount and the customized message for your email.</li>
				<li>Submitting the form will send an email to the client and will be notified. A success message will appear on the top of the page telling the user that it was successfully submitted.</li>
			</ol>
			

			<h3>Adding New Client</h3>
			<ol>
				<li>At Dashboard, under Create Invoice menu, click Add New Client</li>
				<li>Fill at the fields of the form for client details and submit.</li>
			</ol>
			
			<h3>Creating an Invoice</h3>
			<ol>
				<li>At Dashboard, click Create Invoice menu</li>
				<li>You need to add first the client that you are going to send an invoice as you will need your client's company under the select option of our Create Invoice form.</li>
				<li>Fill at the fields of the form and submit. It should send an email to your client.</li>
			</ol>
					
